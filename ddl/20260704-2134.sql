create view VW_MAIL_QUEUE_TO_SEND as
SELECT 1 AS flag_send
    , q.id
    , q.mail_id
    , q.email
    , q.name
    , q.member_insert_date
    , q.status
    , m.from_name AS fromname
    , m.from_email AS fromemail
    , m.subject
    , m.html
FROM wksp_sindicatto.mail_queue q
LEFT JOIN wksp_sindicatto.mail m
          ON m.id = q.mail_id
WHERE 1 = 1
    AND q.status = 'pending'
    AND q.mail_id IN (0036)
    AND q.email IS NOT NULL
    AND NOT exists (SELECT 1
                    FROM wksp_sindicatto.mail_blacklist b
                    WHERE lower(trim(b.email)) = lower(trim(q.email)))
-- AND q.EMAIL IN ('inattox@gmail.com', 'danielmaiax@gmail.com', 'dev@inatto.com')
ORDER BY q.priority
    , q.member_insert_date DESC
    , q.id DESC
