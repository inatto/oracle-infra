create FUNCTION mask_br_cnpj(p_cnpj VARCHAR2)
    RETURN VARCHAR2
    DETERMINISTIC IS
BEGIN
    RETURN REGEXP_REPLACE(p_cnpj, '(\d{2})(\d{3})(\d{3})(\d{4})(\d{2})', '\1.\2.\3/\4-\5');
END;
/

create FUNCTION mask_br_cpf(p_cpf VARCHAR2)
    RETURN VARCHAR2 DETERMINISTIC IS
BEGIN
    IF p_cpf IS NULL OR NOT REGEXP_LIKE(p_cpf, '^[0-9]{11}$') THEN
        RETURN '';
    END IF;
    RETURN SUBSTR(REGEXP_REPLACE(p_cpf, '(\d{3})(\d{3})(\d{3})(\d{2})', '\1.\2.\3-\4'), 1, 20);
END;
/

create FUNCTION mask_br_date(p_date DATE)
    RETURN VARCHAR2 DETERMINISTIC IS
BEGIN
    RETURN TO_CHAR(p_date, 'dd/mm/yyyy');
END;
